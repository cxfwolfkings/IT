function htmlToCanvas(node, options) {
    var html2canvasNodeAttribute = "data-html2canvas-node";
    node.setAttribute(html2canvasNodeAttribute, 1);

    // 1. 复制节点
    var ownerDocument = node.ownerDocument;
    var documentElement = cloneNode(ownerDocument.documentElement);

    var container = ownerDocument.createElement("iframe");
    container.className = "html2canvas-container";
    container.style.visibility = "hidden";
    container.style.position = "fixed";
    container.style.left = "-10000px";
    container.style.top = "0px";
    container.style.border = "0";
    container.width = width;
    container.height = height;
    container.scrolling = "no"; // ios won't scroll without it
    ownerDocument.body.appendChild(container);

    var documentClone = container.contentWindow.document;
    var x = ownerDocument.defaultView.pageXOffset;
    var y = ownerDocument.defaultView.pageYOffset;

    /* Chrome doesn't detect relative background-images assigned in inline <style> sheets when fetched through getComputedStyle
     if window url is about:blank, we can assign the url to current by writing onto the document
     */
    container.contentWindow.onload = container.onload = function () {
        var interval = setInterval(function () {
            if (documentClone.body.childNodes.length > 0) {
                initNode(documentClone.documentElement);
                clearInterval(interval);

                var attributeName = html2canvasNodeAttribute;
                var selector = "[" + attributeName + "='1']";
                ownerDocument.querySelector(selector).removeAttribute(attributeName);
                var clonedWindow = container.contentWindow;
                var containerNode = clonedWindow.document.querySelector(selector);
                var support = new Support(clonedWindow.document);
                var imageLoader = new ImageLoader(support);
                var bounds = getBounds(containerNode);
                var width = documentWidth(clonedWindow.document);
                var height = documentHeight(clonedWindow.document);
                var renderer = new CanvasRenderer(width, height, imageLoader, document);
                var parser = new NodeParser(containerNode, renderer, support, imageLoader);
                parser.ready();
            };
        }, 50);
    };

    documentClone.open();
    documentClone.write("<!DOCTYPE html><html></html>");
    // Chrome scrolls the parent document for some reason after the write to the cloned window???
    restoreOwnerScroll(ownerDocument, x, y);
    documentClone.replaceChild(documentClone.adoptNode(documentElement), documentClone.documentElement);
    documentClone.close();
}

/**
 * 克隆节点
 */
function cloneNode(node) {
    var clone = node.nodeType === 3 ? document.createTextNode(node.nodeValue) : node.cloneNode(false);

    var child = node.firstChild;
    while (child) {
        if (child.nodeType !== 1 || child.nodeName !== 'SCRIPT') {
            clone.appendChild(cloneNode(child));
        }
        child = child.nextSibling;
    }

    if (node.nodeType === 1) {
        clone._scrollTop = node.scrollTop;
        clone._scrollLeft = node.scrollLeft;
        if (node.nodeName === "CANVAS") {
            cloneCanvasContents(node, clone);
        } else if (node.nodeName === "TEXTAREA" || node.nodeName === "SELECT") {
            clone.value = node.value;
        }
    }
    return clone;
}

/**
 * 克隆canvas节点
 */
function cloneCanvasContents(canvas, clonedCanvas) {
    try {
        if (clonedCanvas) {
            clonedCanvas.width = canvas.width;
            clonedCanvas.height = canvas.height;
            clonedCanvas.getContext("2d").putImageData(canvas.getContext("2d").getImageData(0, 0, canvas.width, canvas.height), 0, 0);
        }
    } catch (e) {
        log("Unable to copy canvas content from", canvas, e);
    }
}

/**
 * 初始化节点的位置
 */
function initNode(node) {
    if (node.nodeType === 1) {
        node.scrollTop = node._scrollTop;
        node.scrollLeft = node._scrollLeft;

        var child = node.firstChild;
        while (child) {
            initNode(child);
            child = child.nextSibling;
        }
    }
}

/**
 * 初始化滚动条位置
 */
function restoreOwnerScroll(ownerDocument, x, y) {
    if (ownerDocument.defaultView && (x !== ownerDocument.defaultView.pageXOffset || y !== ownerDocument.defaultView.pageYOffset)) {
        ownerDocument.defaultView.scrollTo(x, y);
    }
}

/**
 * 获取HTML元素节点的位置，大小
 */
function getBounds(node) {
    if (node.getBoundingClientRect) {
        var clientRect = node.getBoundingClientRect();
        var width = node.offsetWidth == null ? clientRect.width : node.offsetWidth;
        return {
            top: clientRect.top,
            bottom: clientRect.bottom || (clientRect.top + clientRect.height),
            right: clientRect.left + width,
            left: clientRect.left,
            width: width,
            height: node.offsetHeight == null ? clientRect.height : node.offsetHeight
        };
    }
    return {};
}

/**
 * 获取HTML文档宽度
 */
function documentWidth(doc) {
    return Math.max(
        Math.max(doc.body.scrollWidth, doc.documentElement.scrollWidth),
        Math.max(doc.body.offsetWidth, doc.documentElement.offsetWidth),
        Math.max(doc.body.clientWidth, doc.documentElement.clientWidth)
    );
}

/**
 * 获取HTML文档高度
 */
function documentHeight(doc) {
    return Math.max(
        Math.max(doc.body.scrollHeight, doc.documentElement.scrollHeight),
        Math.max(doc.body.offsetHeight, doc.documentElement.offsetHeight),
        Math.max(doc.body.clientHeight, doc.documentElement.clientHeight)
    );
}


